package sample.pattern.name.adapter;

/**
 * @author deshp
 *
 */
public class Board {

	private String color;
	
	

	/**
	 * @param color
	 */
	public Board(String color) {
		super();
		this.color = color;
	}

	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}

	/**
	 * @param color the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}
	
	
}
